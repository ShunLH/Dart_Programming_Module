void main() {
  
  //Arithmetic Operator
  
  print(44 + 2);
  print(44 - 2);
  print(44 * 2);
  print(44 / 2);
  
  //Equality Operator
  
  print(42 == 41);
  print(42 != 41);
  
  //Increment and Decrement Operator
  var number = 2;
  print(++number);
  print(number++);
  
  var anotherNumber = 4;
  print(--anotherNumber);
  print(anotherNumber--);
  
  //Compound Assignment Operator
  var firstNumber = 5;
  firstNumber += 1;
  print(firstNumber);
  
  var secondNumber = 5;
  secondNumber -= 1;
  print(secondNumber);
  
  //Comparison Operator
  print(42 < 50);
  print(42 <= 50);
  print(42 > 30);
  print(42 >= 42);
  
  //Modulo Operator
  print(25 % 2);
  
  //Logical Operator
  print(24 < 25 && 24 < 21);
  print(24 < 25 || 24 < 21);

  //Negation Operator
  print(!(24 < 25));

  
}